def _clean_column_names(df: pd.DataFrame) -> pd.DataFrame:
    df.columns = (
        df.columns
        .str.strip()
        .str.lower()
        .str.replace(r"[^\w]", "_", regex=True)
        .str.replace(r"_+", "_", regex=True)
        .str.strip("_")
    )
    return df


def download_all_datasets(base_dir: str = None, force: bool = False, verbose: bool = True) -> dict:
    """
    Download all 8 UCI engineering datasets via ucimlrepo.

    Returns a dict mapping dataset_name -> pd.DataFrame (raw).
    """
    from ucimlrepo import fetch_ucirepo

    base_dir = base_dir or _BASE_DIR
    results = {}
    log_entries = []

    for name, cfg in UCI_REGISTRY.items():
        local_dir = os.path.join(base_dir, cfg["local_dir"])
        raw_path = os.path.join(local_dir, "raw", f"{name}_raw.parquet")
        os.makedirs(os.path.join(local_dir, "raw"), exist_ok=True)
        os.makedirs(os.path.join(local_dir, "processed"), exist_ok=True)

        if os.path.exists(raw_path) and not force:
            if verbose:
                print(f"  [{name}] Already cached — loading from disk")
            df = pd.read_parquet(raw_path)
        else:
            if verbose:
                print(f"  [{name}] Downloading from UCI (ID={cfg['uci_id']}) ...")
            try:
                repo = fetch_ucirepo(id=cfg["uci_id"])
                X_raw = repo.data.features
                y_raw = repo.data.targets
                df = pd.concat([X_raw, y_raw], axis=1)
                df.to_parquet(raw_path, index=False)
                if verbose:
                    print(f"           Saved {len(df)} rows × {df.shape[1]} cols → {raw_path}")
            except Exception as e:
                print(f"  [{name}] FAILED: {e}")
                continue

        results[name] = df
        log_entries.append({
            "dataset": name,
            "uci_id": cfg["uci_id"],
            "rows": len(df),
            "cols": df.shape[1],
            "downloaded_at": datetime.now().isoformat(),
            "status": "ok",
        })

    # Save download log
    log_path = os.path.join(base_dir, "download_log.json")
    with open(log_path, "w") as f:
        json.dump(log_entries, f, indent=2)

    if verbose:
        print(f"\nDownload complete. {len(results)}/{len(UCI_REGISTRY)} datasets ready.")

    return results


def load_dataset(
    name: str,
    base_dir: str = None,
    max_features: int = 7,
    verbose: bool = True,
) -> tuple:
    """
    Load, preprocess, and return (X, y, feature_names, metadata) for a named dataset.

    Preprocessing steps:
      1. Load from raw cache (downloading if necessary)
      2. Drop specified columns
      3. Remove rows with missing values
      4. Extract target column
      5. Return raw X and y (feature selection happens inside CV folds)

    Parameters
    ----------
    name : str
        Dataset key from UCI_REGISTRY.
    base_dir : str, optional
        Root datasets directory.
    max_features : int
        Informational only — actual MI-based selection done in experiment loops.
    verbose : bool

    Returns
    -------
    X : np.ndarray, shape (n_samples, n_features)
    y : np.ndarray, shape (n_samples,)
    feature_names : list[str]
    metadata : dict
    """
    base_dir = base_dir or _BASE_DIR
    cfg = UCI_REGISTRY[name]

    raw_path = os.path.join(base_dir, cfg["local_dir"], "raw", f"{name}_raw.parquet")
    if not os.path.exists(raw_path):
        if verbose:
            print(f"  [{name}] Not cached — downloading ...")
        download_all_datasets(base_dir=base_dir, force=False, verbose=verbose)

    df = pd.read_parquet(raw_path)

    # Drop specified columns
    drop = [c for c in cfg["drop_cols"] if c in df.columns]
    if drop:
        df = df.drop(columns=drop)

    # Handle missing values
    n_before = len(df)
    df = df.dropna()
    n_dropped = n_before - len(df)
    if n_dropped > 0 and verbose:
        print(f"  [{name}] Dropped {n_dropped} rows with missing values")

    # Identify target
    target_col = cfg["target"]
    # Handle case where column name was cleaned
    if target_col not in df.columns:
        df_clean = _clean_column_names(df.copy())
        target_clean = (
            target_col.strip().lower()
            .replace(" ", "_").replace("-", "_")
            .replace(",", "_").replace("(", "_").replace(")", "_")
        )
        # Try partial match
        candidates = [c for c in df_clean.columns if target_clean[:8] in c]
        if candidates:
            target_col_orig = df.columns[df_clean.columns.get_loc(candidates[0])]
            target_col = target_col_orig
        else:
            raise ValueError(f"Target column '{cfg['target']}' not found. Columns: {df.columns.tolist()}")

    y = df[target_col].values.astype(float)
    X_df = df.drop(columns=[target_col])
    # Keep only numeric columns
    X_df = X_df.select_dtypes(include=[np.number])
    X = X_df.values.astype(float)
    feature_names = X_df.columns.tolist()

    metadata = {
        "name": name,
        "description": cfg["description"],
        "domain": cfg["domain"],
        "target": cfg["target_alias"],
        "n_samples": len(df),
        "n_features_raw": len(feature_names),
        "max_features_interpretability": max_features,
        "engineering_context": cfg["engineering_context"],
    }

    if verbose:
        print(f"  [{name}] {len(df)} samples, {len(feature_names)} features, "
              f"target='{cfg['target_alias']}' "
              f"[{y.min():.3f}, {y.max():.3f}]")

    return X, y, feature_names, metadata
